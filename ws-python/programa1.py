#declarando tipo de variavel (não é obrigatório o python sabe interpretar)
#Python aceita atribuição diretamente na declaração da variável, e também aceita omissão do tipo.

idade: int
salario: float
altura: float
genero: str
nome: str

#declarando tipo de variavel


idade = 21
salario = 5800.5
altura = 1.63
genero = "F"
nome = "Hellen Silva"

print(f"IDADE = {idade}")
print(f"SALARIO = {salario:.2f}")
print(f"ALTURA = {altura:.2f}")
print(f"GENERO = {genero}")
print(f"NOME = {nome}")
